package AT14;

public class AT14 {

	public static void main(String[] args) {

		int soma = 0;
		int i;

		do {
			
			
			for (i = 0; i <= 100; i++) {
			   soma += i;   
			}
			System.out.println("Soma: " + soma);

		} while (i <= 100);

		System.out.println("FIM");

	}

}
